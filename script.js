// Search Functionality for Index Page
document.getElementById("search-bar")?.addEventListener("input", function() {
    let query = this.value.toLowerCase();
    let subjects = document.querySelectorAll(".subject-card");

    subjects.forEach(subject => {
        subject.style.display = subject.textContent.toLowerCase().includes(query) ? "block" : "none";
    });
});
document.getElementById('search-button').addEventListener('click', function() {
    const query = document.getElementById('search-bar').value.toLowerCase();
    const subjects = document.querySelectorAll('.subject-card');

    subjects.forEach(subject => {
        if (subject.textContent.toLowerCase().includes(query)) {
            subject.style.display = 'block';
        } else {
            subject.style.display = 'none';
        }
    });
});
document.addEventListener("DOMContentLoaded", function () {
    // Check if we are on the "Highlights" page
    if (window.location.pathname.includes("important-topics.html")) {
        const container = document.getElementById("highlight-grid");

        if (container) {
            Object.keys(cbtData).forEach(subject => {
                // Create a subject card
                const card = document.createElement("div");
                card.classList.add("subject-card");

                // Add subject title
                const title = document.createElement("h3");
                title.textContent = subject.toUpperCase();
                card.appendChild(title);

                // Create a container for highlighted topics
                const topicList = document.createElement("div");
                topicList.classList.add("highlighted-topics");

                // Add each highlighted topic as a separate small card
                cbtData[subject].forEach(topic => {
                    const topicCard = document.createElement("div");
                    topicCard.classList.add("topic-card");
                    topicCard.textContent = topic.title; // Display topic name
                    topicList.appendChild(topicCard);
                });

                // Append the topic list to the subject card
                card.appendChild(topicList);
                container.appendChild(card);
            });
        }
    }
});
        

